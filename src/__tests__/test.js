import healthStatus from '../main';

test('Helthbar status function', () => {
  const unitsList = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const expected = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];

  expect(healthStatus(unitsList)).toEqual(expected);
});
